#ifndef CONTIKI_MACROS_H_
#define CONTIKI_MACROS_H_

/**
 * This macro ensures, that the access to the variable can not be reordered
 * by the compiler.
 * See https://lwn.net/Articles/508991/
 */
#define ACCESS_ONCE(x) (*(volatile typeof(x) *)&(x))


#endif /* CONTIKI_MACROS_H_ */
